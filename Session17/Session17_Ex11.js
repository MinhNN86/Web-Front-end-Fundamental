// Math.abs() trả về giá trị tuyệt đối của số, loại bỏ dấu âm.
console.log(Math.abs(-5, 5)); // Trả về giá trị tuyệt đối của 5 vì không lấy dấu ,
console.log(Math.abs(10)); // Trả về giá trị tuyệt đối của 10 là 10
// Math.ceil() luôn làm tròn lên, dù phần thập phân nhỏ hơn 0.5.
console.log(Math.ceil(4.4)); // Làm tròn lên, 4.4 -> 5
console.log(Math.ceil(5.8)); // Làm tròn lên, 5.8 -> 6
// Math.floor() luôn làm tròn xuống, bỏ phần thập phân.
console.log(Math.floor(5.7)); // Làm tròn xuống, 5.7 -> 5
console.log(Math.floor(6.1)); // Làm tròn xuống, 6.1 -> 6
// Math.round() làm tròn về số nguyên gần nhất.
console.log(Math.round(10.1)); // Làm tròn chuẩn, 10.1 gần 10 hơn -> 10
console.log(Math.round(20.6)); // Làm tròn chuẩn, 20.6 gần 21 hơn -> 21
